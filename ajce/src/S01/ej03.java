package S01;
/**
* 1. Salida por pantalla.
*
* Escribe un programa que muestre por pantalla 10 palabras en inglés junto a su
* correspondiente traducción al castellano. Las palabras deben estar distribuidas
* en dos columnas y alineadas a la izquierda.
*
*
* @author Luis José Sánchez
*/

public class ej03 {

	public static void main(String[] args) {
		System.out.println("Hello:\t Hola");
		System.out.println("Goodbye: Adiós");
		System.out.println("Friend:\t Amigo");
		System.out.println("Family:\t Familia");
		System.out.println("Love:\t Amor");
		System.out.println("Home:\t Hogar");
		System.out.println("Time:\t Tiempo");
		System.out.println("Work:\t Trabajo");
		System.out.println("School:\t Escuela");
		System.out.println("Day:\t Día");		
	}
}
